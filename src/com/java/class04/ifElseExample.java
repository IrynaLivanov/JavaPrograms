package com.java.class04;

public class ifElseExample {
    public static void main(String[] args) {
        int a = 11;
        boolean b = a >= 11;

        if (b == true) {
            System.out.print ("2");

        } else {
            System.out.println("3");
        }
        System.out.println("4");
    }
}
