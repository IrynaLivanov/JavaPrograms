package com.java.class03;

public class homeW3_3 {
    public static void main(String[] args) {
        int a = 1;
        int b = -1;
        System.out.println(a > b); //true
        System.out.println(a < b); //false
        System.out.println(a >= b); //true
        System.out.println(a <= b); //false
        System.out.println(a != b); //true
        System.out.println(a == b); //false
    }
}
